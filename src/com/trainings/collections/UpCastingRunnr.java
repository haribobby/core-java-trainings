package com.trainings.collections;

public class UpCastingRunnr {

	public static void main(String[] args) {
		
		int a = 10; // small/sub
		
		
		long d  = a; //big/Super
		
		//upcasting
		
		System.out.println(d);
		
		
		double b = 20.89;
		
		int i = (int)b;
		
		//down casting
		
		
		System.out.println(i);
	}
}
